function ensureDir(dir) {
    if (!fs.existsSync(dir)) {
        fs.mkdirSync(dir, { recursive: true });
    }
}

function copyFileSync(src, dest) {
    if (fs.existsSync(src)) {
        fs.copyFileSync(src, dest);
        return true;
    }
    return false;
}

function safeName(name) {
    return name.replace(/[^a-zA-Z0-9]/g, '_');
}

function calculateSizes(totalWeight) {
    return [
        { id: 'full',    name: 'Full',    weight: totalWeight,       multiplier: 1.0,   percentage: 100 },
        { id: 'half',    name: 'Half',    weight: totalWeight / 2,   multiplier: 0.5,   percentage: 50 },
        { id: 'quarter', name: 'Quarter', weight: totalWeight / 4,   multiplier: 0.25,  percentage: 25 },
        { id: 'eighth',  name: 'Eighth',  weight: totalWeight / 8,  multiplier: 0.125, percentage: 12.5 }
    ];
}

function generateProductsJs(products, slippageRate) {
    // Accept either a single product object (legacy) or an array
    const productArray = Array.isArray(products) ? products : [products];
    const slippage = typeof slippageRate === 'number' ? slippageRate : 10;

    const productsJson = productArray.map((product, idx) => {
        const sizes = calculateSizes(
            product.mode === 'units' ? product.maxUnits : product.weight
        );
        const imageFile = productArray.length === 1 ? 'item.jpg' : `item_${idx}.jpg`;
        return `    {
        name: ${JSON.stringify(product.name)},
        description: ${JSON.stringify(product.description)},
        mode: ${JSON.stringify(product.mode || 'weight')},
        pricePerUnit: ${product.pricePerUnit || 0},
        pricePerGram: ${product.pricePerGram || 0},
        totalWeight: ${product.weight || 0},
        maxUnits: ${product.maxUnits || 10},
        image: ${JSON.stringify(imageFile)},
        sizes: ${JSON.stringify(sizes, null, 8).replace(/\n/g, '\n        ')},
        rebroadcastDelayHours: ${product.rebroadcastDelayHours || 2},
        rebroadcastMaxIntervalHours: ${product.rebroadcastMaxIntervalHours || 24},
        firstRebroadcastDelayHours: ${product.firstRebroadcastDelayHours || 2},
        created: ${JSON.stringify(new Date().toISOString())}
    }`;
    }).join(',\n');

    return `// Product Configuration - Generated by miniMerch CLI
// DO NOT EDIT - Regenerate using: mini-merch generate or mini-merch generate-multi

// Slippage buffer applied to Minima payments at checkout (vendor-defined %)
const SLIPPAGE_RATE = ${slippage};

const PRODUCTS = [
${productsJson}
];

// Backward-compat alias — single-product shops use this directly
// Declared as let so the carousel can reassign it when navigating products
let PRODUCT = PRODUCTS[0];

function calculateSizesForWeight(totalWeight) {
    return [
        { id: 'full',    name: 'Full',    weight: totalWeight,       multiplier: 1.0,   percentage: 100 },
        { id: 'half',    name: 'Half',    weight: totalWeight / 2,   multiplier: 0.5,   percentage: 50 },
        { id: 'quarter', name: 'Quarter', weight: totalWeight / 4,   multiplier: 0.25,  percentage: 25 },
        { id: 'eighth',  name: 'Eighth',  weight: totalWeight / 8,   multiplier: 0.125, percentage: 12.5 }
    ];
}

function getPriceForSize(sizeId, product) {
    const p = product || PRODUCT;
    const size = p.sizes.find(s => s.id === sizeId);
    if (!size) return 0;
    if (p.mode === 'units') return p.pricePerUnit * size.weight;
    return p.pricePerGram * size.weight;
}
`;
}

