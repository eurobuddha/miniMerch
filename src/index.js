// products: single product object (legacy) or array of up to 8
// imagePaths: single path string (legacy) or array of paths matching products
async function buildShopMiniDapp(products, imagePaths, slippageRate) {
    const productArray = Array.isArray(products) ? products : [products];
    const imagePathArray = Array.isArray(imagePaths) ? imagePaths : [imagePaths];
    const slippage = typeof slippageRate === 'number' ? slippageRate : 10;
    const isMulti = productArray.length > 1;

    ensureDir(OUTPUT_DIR);
    const tempDir = path.join(OUTPUT_DIR, 'temp_shop_' + Date.now());
    ensureDir(tempDir);

    console.log(`\n📦 Building miniMerch Shop MiniDapp (${productArray.length} product${productArray.length > 1 ? 's' : ''})...\n`);

    const vendorConfig = loadConfig();
    if (!vendorConfig) {
        console.error('\n❌ Error: Vendor config not found. Run "mini-merch setup" first.\n');
        process.exit(1);
    }

    const files = fs.readdirSync(SHOP_TEMPLATE_DIR);
    for (const file of files) {
        const src = path.join(SHOP_TEMPLATE_DIR, file);
        const dest = path.join(tempDir, file);

        if (file === 'products.js') {
            fs.writeFileSync(dest, generateProductsJs(productArray, slippage));
            console.log(`   ✓ Generated: ${file} (slippage: ${slippage}%)`);
        } else if (file === 'config.js') {
            fs.writeFileSync(dest, generateConfigJs(vendorConfig));
            console.log(`   ✓ Generated: ${file} (with obfuscated address)`);
        } else if (file === 'app.js') {
            let content = fs.readFileSync(src, 'utf8');
            content = content.replace("const OBFUSCATED_CMC_KEY = '';", `const OBFUSCATED_CMC_KEY = '${vendorConfig.obfuscatedApiKey}';`);
            content = content.replace("const CMC_KEY_SALT = '';", `const CMC_KEY_SALT = '${vendorConfig.salt}';`);
            fs.writeFileSync(dest, content);
            console.log(`   ✓ Generated: ${file} (with obfuscated API key)`);
        } else if (file === 'dapp.conf') {
            fs.writeFileSync(dest, generateShopDappConf(productArray[0]));
            console.log(`   ✓ Generated: ${file}`);
        } else if (file === 'item.jpg' && isMulti) {
            // Skip default image for multi-product — images are item_0.jpg, item_1.jpg etc.
        } else {
            copyFileSync(src, dest);
            console.log(`   ✓ Copied: ${file}`);
        }
    }

    // Copy product images
    if (isMulti) {
        productArray.forEach((product, idx) => {
            const imgPath = imagePathArray[idx];
            if (imgPath && fs.existsSync(imgPath)) {
                const ext = path.extname(imgPath);
                copyFileSync(imgPath, path.join(tempDir, `item_${idx}${ext}`));
                console.log(`   ✓ Copied: item_${idx}${ext} (${product.name})`);
            } else {
                // Fall back to default item.jpg, renamed to item_N.jpg
                const defaultImg = path.join(SHOP_TEMPLATE_DIR, 'item.jpg');
                if (fs.existsSync(defaultImg)) {
                    copyFileSync(defaultImg, path.join(tempDir, `item_${idx}.jpg`));
                    console.log(`   ✓ Copied: item_${idx}.jpg (default, ${product.name})`);
                }
                if (imgPath) console.warn(`   ⚠️  Warning: Image not found at ${imgPath}`);
            }
        });
    } else {
        // Single product — legacy behaviour
        const imagePath = imagePathArray[0];
        if (imagePath) {
            if (fs.existsSync(imagePath)) {
                const ext = path.extname(imagePath);
                const destImage = path.join(tempDir, 'item' + ext);
                copyFileSync(imagePath, destImage);
                if (ext !== '.jpg') {
                    let content = fs.readFileSync(path.join(tempDir, 'products.js'), 'utf8');
                    content = content.replace('item.jpg', 'item' + ext);
                    fs.writeFileSync(path.join(tempDir, 'products.js'), content);
                }
                console.log(`   ✓ Copied: item${ext}`);
            } else {
                console.warn(`   ⚠️  Warning: Image not found at ${imagePath}`);
            }
        }
    }

    const zipName = isMulti
        ? `miniMerch_Shop.mds.zip`
        : `miniMerch_${safeName(productArray[0].name)}.mds.zip`;
    const zipPath = path.join(OUTPUT_DIR, zipName);

    return new Promise((resolve, reject) => {
        const output = fs.createWriteStream(zipPath);
        const archive = archiver('zip', { zlib: { level: 9 } });
        output.on('close', () => {
            console.log(`\n✅ Shop MiniDapp created!`);
            console.log(`📁 Output: ${zipPath}`);
            console.log(`📊 Size: ${(archive.pointer() / 1024).toFixed(2)} KB\n`);
            fs.rmSync(tempDir, { recursive: true, force: true });
            resolve(zipPath);
        });
        archive.on('error', reject);
        archive.pipe(output);
        archive.directory(tempDir, false);
        archive.finalize();
    });
}

