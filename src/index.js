async function main() {
    const subcommand = process.argv[2];

    if (subcommand === 'studio') {
        require('./studio').start();
        return;
    }

    if (subcommand === 'setup') {
        runSetup();
        return;
    }

    if (subcommand === 'config') {
        runConfig(process.argv);
        return;
    }

    if (subcommand === 'generate-multi') {
        await runGenerateMulti();
        return;
    }

    if (subcommand === 'generate') {
        const program = new Command();
        program
            .name('mini-merch generate')
            .description('Generate miniMerch MiniDapps')
            .option('-n, --name <name>', 'Product name')
            .option('-m, --mode <mode>', 'Sales mode: weight or units', 'weight')
            .option('-p, --price <price>', 'Price (per gram or per unit)')
            .option('-w, --weight <weight>', 'Total weight in grams (weight mode)')
            .option('-u, --units <units>', 'Max units per "full" (units mode)', parseInt)
            .option('-d, --desc <description>', 'Product description')
            .option('-i, --image <path>', 'Path to product image')
            .option('--inbox-only', 'Generate only the inbox MiniDapp')
            .option('--rebroadcast-delay <hours>', 'Hours before first rebroadcast attempt', parseInt, 2)
            .option('--max-rebroadcast <hours>', 'Max hours between rebroadcast attempts', parseInt, 24)
            .option('--slippage <percent>', 'Slippage % added to Minima payments at checkout (default 10)', parseFloat, 10);
        program.parse(process.argv);
        await runGenerate(program.opts());
        return;
    }

    const program = new Command();
    program
        .name('mini-merch')
        .description('Generate miniMerch MiniDapps for the Minima blockchain')
        .version('0.1.0');

    program
        .command('setup <address> <apikey> <pubkey> [inboxpubkey]')
        .description('Save vendor configuration (one-time setup)')
        .action(() => runSetup());

    program
        .command('generate')
        .description('Generate shop and/or inbox MiniDapps')
        .option('-n, --name <name>', 'Product name')
        .option('-m, --mode <mode>', 'Sales mode: weight or units', 'weight')
        .option('-p, --price <price>', 'Price (per gram or per unit)')
        .option('-w, --weight <weight>', 'Total weight in grams (weight mode)')
        .option('-u, --units <units>', 'Max units per "full" (units mode)', parseInt)
        .option('-d, --desc <description>', 'Product description')
        .option('-i, --image <path>', 'Path to product image')
        .option('--inbox-only', 'Generate only the inbox MiniDapp')
        .option('--rebroadcast-delay <hours>', 'Hours before first rebroadcast attempt', parseInt, 2)
        .option('--max-rebroadcast <hours>', 'Max hours between rebroadcast attempts', parseInt, 24)
        .option('--slippage <percent>', 'Slippage % added to Minima payments at checkout (default 10)', parseFloat, 10)
        .action(async (opts) => await runGenerate(opts));

    program
        .command('generate-multi')
        .description('Interactively build a shop with up to 8 products (carousel)')
        .action(async () => await runGenerateMulti());

    program
        .command('studio')
        .description('Open the miniMerch Studio web UI to build shops visually')
        .action(() => require('./studio').start());

    program
        .command('config')
        .description('Manage stored configuration')
        .option('--show', 'Display current config')
        .option('--reset', 'Delete stored config')
        .action((opts) => {
            const args = process.argv.slice(3);
            if (opts.reset) args.push('--reset');
            if (opts.show) args.push('--show');
            runConfig(args);
        });

    program.parse(process.argv);
}

main().catch(console.error);
