// ─── Interactive Multi-Product Wizard ────────────────────────────────────────

function ask(rl, question) {
    return new Promise(resolve => rl.question(question, resolve));
}

async function promptProduct(rl, idx) {
    console.log(`\n── Product ${idx + 1} ──────────────────────────────────────`);

    const name = (await ask(rl, '  Name: ')).trim();
    if (!name) return null; // empty name = done entering products

    const modeRaw = (await ask(rl, '  Mode [weight/units] (default: weight): ')).trim().toLowerCase();
    const mode = modeRaw === 'units' ? 'units' : 'weight';

    const priceRaw = (await ask(rl, `  Price per ${mode === 'units' ? 'unit' : 'gram'} (USD): `)).trim();
    const price = parseFloat(priceRaw);
    if (isNaN(price) || price <= 0) {
        console.error('  ❌ Invalid price. Skipping this product.');
        return null;
    }

    let weight = 0, maxUnits = 10;
    if (mode === 'weight') {
        const wRaw = (await ask(rl, '  Total batch weight in grams: ')).trim();
        weight = parseFloat(wRaw);
        if (isNaN(weight) || weight <= 0) {
            console.error('  ❌ Invalid weight. Skipping this product.');
            return null;
        }
    } else {
        const uRaw = (await ask(rl, '  Max units available: ')).trim();
        maxUnits = parseInt(uRaw);
        if (isNaN(maxUnits) || maxUnits < 1) {
            console.error('  ❌ Invalid units. Skipping this product.');
            return null;
        }
    }

    const desc = (await ask(rl, '  Description (optional): ')).trim() || 'Premium product';
    const image = (await ask(rl, '  Image path (optional, press Enter to skip): ')).trim() || null;

    return {
        name,
        mode,
        pricePerGram: mode === 'weight' ? price : 0,
        pricePerUnit: mode === 'units' ? price : 0,
        weight: mode === 'weight' ? weight : 0,
        maxUnits: mode === 'units' ? maxUnits : 10,
        description: desc,
        firstRebroadcastDelayHours: 2,
        rebroadcastMaxIntervalHours: 24,
        image
    };
}

