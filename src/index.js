function runConfig(args) {
    if (args.includes('--reset')) {
        const existing = loadConfig();
        if (!existing) {
            console.log('No config file found. Nothing to delete.');
            return;
        }
        deleteConfig();
        console.log(`✅ Config deleted from: ${getConfigPath()}`);
        return;
    }

    if (args.includes('--show')) {
        const existing = loadConfig();
        if (!existing) {
            console.log(`No config found. Run: mini-merch setup <address> <apikey> <pubkey>`);
            return;
        }
        console.log(`Config file: ${getConfigPath()}`);
        console.log(`Created: ${existing.created}`);
        console.log(`Address:    ${maskString(existing.obfuscated, 20, 20)} (obfuscated)`);
        console.log(`Pub Key:   ${maskString(existing.vendorPublicKey, 12, 8)}`);
        return;
    }

    console.log(`Config file: ${getConfigPath()}`);
    console.log(`Use --show to display current config, or --reset to delete it.`);
}

