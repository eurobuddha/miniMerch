function validateInputs(options) {
    const errors = [];
    if (!options.name) errors.push('Product name is required (use -n or --name)');
    const mode = options.mode || 'weight';
    if (mode === 'units') {
        if (!options.price && options.price !== 0) errors.push('Price per unit is required (use -p or --price)');
        if (options.price && isNaN(parseFloat(options.price))) errors.push('Price must be a number');
        if (options.price && parseFloat(options.price) <= 0) errors.push('Price must be greater than 0');
        if (!options.units && options.units !== 0) errors.push('Max units is required for units mode (use -u or --units)');
        if (options.units && (isNaN(parseInt(options.units)) || parseInt(options.units) < 1)) {
            errors.push('Max units must be at least 1');
        }
    } else {
        if (!options.price && options.price !== 0) errors.push('Price per gram is required (use -p or --price)');
        if (options.price && isNaN(parseFloat(options.price))) errors.push('Price must be a number');
        if (options.price && parseFloat(options.price) <= 0) errors.push('Price must be greater than 0');
        if (!options.weight && options.weight !== 0) errors.push('Weight is required for weight mode (use -w or --weight)');
        if (options.weight && isNaN(parseFloat(options.weight))) errors.push('Weight must be a number');
        if (options.weight && parseFloat(options.weight) <= 0) errors.push('Weight must be greater than 0');
    }
    return errors;
}

